describe('operation :: workspace > delete > html-document', function () {
    before(function () {
        cy.visit('/login')
        cy.get('input[type=email]').type('admin@admin.admin')
        cy.get('input[type=password]').type('admin@admin.admin')
        cy.get('form').find('button').get('.connection__form__btnsubmit').click()
    })
    it ('all content > html-document > delete first html-doc', function(){
        cy.get('.sidebar__content__navigation__workspace__item__submenu > li:nth-child(2)').click()
        cy.get('.pageTitleGeneric__title__icon').should('be.visible')
        cy.get('.content__name__text').contains('dashboard button').should('be.visible')
        cy.get('.content__name__text').contains('dashboard button').click()
        cy.get('#appFeatureContainer .html-document.visible ').should('be.visible')
        cy.get('.align-items-center button:nth-child(2)').click()
        cy.get('.html-document__contentpage__textnote__state__btnrestore').should('be.visible')
        cy.get('.html-document__header__close').click()
        cy.get('#appFeatureContainer .html-document.visible ').should('not.be.visible')
        cy.get('.content__name__text').contains('dashboard button').should('not.be.visible')
    })
    it ('all content > html-document > delete second html-doc', function(){
        cy.get('.sidebar__content__navigation__workspace__item__submenu > li:nth-child(2)').click()
        cy.get('.pageTitleGeneric__title__icon').should('be.visible')
        cy.get('.content__name__text').contains('all content button').should('be.visible')
        cy.get('.content__name__text').contains('all content button').click()
        cy.get('#appFeatureContainer .html-document.visible ').should('be.visible')
        cy.get('.align-items-center button:nth-child(2)').click()
        cy.get('.html-document__contentpage__textnote__state__btnrestore').should('be.visible')
        cy.get('.html-document__header__close').click()
        cy.get('#appFeatureContainer .html-document.visible ').should('not.be.visible')
        cy.get('.content__name__text').contains('all content button').should('not.be.visible')
    })
})